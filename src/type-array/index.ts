// The type Array is a utility type that represents an array of a specific type. It is defined as follows:

let numbers: number[] = [1, 2, 3, 4, 5];

let strings: string[] = ["hello", "world"];

// You can also use the Array<T> syntax to define an array of a specific type:

let booleans: Array<boolean> = [true, false, true];

// The type Array can also be used with generics to create arrays of any type, but normally
// the type[] syntax is preferred for its simplicity and readability.

